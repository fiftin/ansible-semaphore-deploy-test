#!/bin/bash

# Указываем путь к файлу
FILE="/tmp/my_new_file.txt"

# Проверяем, существует ли файл
if [ -f "$FILE" ]; then
    echo "Файл $FILE уже существует."
else
    # Создаем файл и записываем в него текст
    echo "Это новый файл!" > "$FILE"
    echo "Файл $FILE был создан и в него записан текст."
fi
